function openSpecificSlider(photoId, cameFromMenu = false) {
    const item = appItems.find(p => p.type === 'photo' && p.id === photoId);
    modalTitle.innerText = item.title;
    modalTag.innerText = item.tag;
    progressBar.style.width = "100__"; // full bar for photo slider

    currentPhotoSet = item.images;
    currentSubIndex = 0;

    // वापस जाने का बटन सिर्फ तभी दिखेगा जब यूज़र अंदरूनी मेनू से आया हो
    let backBtnHtml = cameFromMenu ? `
        <button onclick="openPhotosMenu()" style="background: rgba(56, 189, 248, 0.15); border: none; color: #38bdf8; padding: 0.5rem 1rem; border-radius: 20px; font-weight: 600; cursor: pointer; margin-bottom: 1rem; display: inline-block;">
            ← Back to Gallery Menu
        </button>
    ` : '';

    modalBody.innerHTML = `
        ${backBtnHtml}
        <div class="slider-wrapper" style="margin-top: 0.5rem;">
            <button class="slide-btn prev-btn" onclick="changeSubSlide(-1)">&#10094;</button>
            <img src="${currentPhotoSet[currentSubIndex]}" class="slider-img" id="subSliderImage">
            <button class="slide-btn next-btn" onclick="changeSubSlide(1)">&#10095;</button>
        </div>
        <p style="color: #cbd5e1; font-size: 0.95rem; margin-top: 1rem; text-align: center; font-style: italic; line-height: 1.5; padding: 0 10px;">
            "${item.excerpt}"
        </p>
    `;
    modal.classList.add('active');
}

function changeSubSlide(direction) {
    currentSubIndex += direction;
    if (currentSubIndex >= currentPhotoSet.length) currentSubIndex = 0;
    if (currentSubIndex < 0) currentSubIndex = currentPhotoSet.length - 1;
    document.getElementById('subSliderImage').src = currentPhotoSet[currentSubIndex];
}

function closeModal() {
    modal.classList.remove('active');
}

function updateProgress(element) {
    const totalScrollHeight = element.scrollHeight - element.clientHeight;
    if (totalScrollHeight > 0) {
        const scrolledPercentage = (element.scrollTop / totalScrollHeight) * 100;
        progressBar.style.width = scrolledPercentage + "%";
    }
}

modal.addEventListener('click', (e) => { if(e.target === modal) closeModal(); });
  
