// --- TYPEWRITER AUTO-EFFECT ---
const words = ["Your Pure Feelings.", "Amazing Photos.", "Deep Shayari."];
let i = 0, timer, txtElement = document.getElementById("typewriter");

function typingEffect() {
    let word = words[i].split("");
    var loopTyping = function() {
        if (word.length > 0) { txtElement.innerHTML += word.shift(); } 
        else { setTimeout(deletingEffect, 2000); return false; }
        timer = setTimeout(loopTyping, 100);
    };
    loopTyping();
}

function deletingEffect() {
    let word = words[i].split("");
    var loopDeleting = function() {
        if (word.length > 0) { word.pop(); txtElement.innerHTML = word.join(""); } 
        else { i = (i + 1) % words.length; setTimeout(typingEffect, 500); return false; }
        timer = setTimeout(loopDeleting, 60);
    };
    loopDeleting();
}
typingEffect();

